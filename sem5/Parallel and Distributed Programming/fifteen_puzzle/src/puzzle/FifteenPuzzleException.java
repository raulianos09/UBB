package puzzle;


public class FifteenPuzzleException extends Exception {
    public FifteenPuzzleException() { }

    public FifteenPuzzleException(String message) { super(message); }

    public FifteenPuzzleException(String message, Throwable cause) { super(message, cause); }

    public FifteenPuzzleException(Throwable cause) { super(cause); }
}